import turtle

turtle.goto(90,0)
turtle.dot()
turtle.penup()
turtle.forward(-10)
turtle.pendown()
turtle.write('South')
turtle.penup()
turtle.goto(0,0)
turtle.pendown()
turtle.goto(0,0)
turtle.goto(-90,0)
turtle.dot()
turtle.penup()
turtle.forward(-10)
turtle.pendown()
turtle.write('West')
turtle.penup()
turtle.goto(0,0)
turtle.pendown()
turtle.goto(0,0)
turtle.setheading(90)
turtle.forward(90)
turtle.dot()
turtle.penup()
turtle.forward(5)
turtle.pendown()
turtle.write('North')
turtle.penup()
turtle.goto(0,0)
turtle.pendown()
turtle.goto(0,0)
turtle.setheading(270)
turtle.forward(90)
turtle.dot()
turtle.penup()
turtle.forward(18)
turtle.pendown()
turtle.write('South')
turtle.penup()
turtle.goto(0,0)
turtle.pendown()
turtle.hideturtle()
turtle.done()